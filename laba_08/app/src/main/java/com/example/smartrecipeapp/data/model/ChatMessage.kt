// 1. ПАКЕТ: Файл лежить у папці з моделями даних
package com.example.smartrecipeapp.data.model

// 2. DATA CLASS: У Kotlin це спеціальний клас для зберігання інформації.
// Він автоматично генерує методи toString(), equals(), copy().
// Тобі не треба писати геттери/сеттери вручну — все працює "з коробки".
data class ChatMessage(

    // 3. ЗМІСТ: Саме повідомлення (наприклад, "Привіт" або рецепт борщу).
    val text: String,

    // 4. АВТОРСТВО: Логічний перемикач.
    // true -> Це написав ТИ (повідомлення буде праворуч, синє).
    // false -> Це написав ШЕФ (повідомлення буде ліворуч, сіре).
    val isUser: Boolean,

    // 5. СТАТУС ПОМИЛКИ (зі значенням за замовчуванням):
    // "= false" означає, що якщо ми не вкажемо цей параметр при створенні,
    // він автоматично буде false (тобто "все ок").
    // Ми ставимо true тільки якщо стався збій (наприклад, немає інтернету),
    // щоб підсвітити повідомлення червоним.
    val isError: Boolean = false
)